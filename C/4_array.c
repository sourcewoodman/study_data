#include <stdio.h>

// 数组
void array() {
    // 定义格式: 数据类型 数组名[长度]
    int arr1[80];
    int arr2[80];
    // 引用
    // 一维数组: 数组名[索引]
    arr1[0] = 1;

    // 二维数组: 数组名[索引][索引]

    // 赋值
    // 定义的时候赋值
    int a1[1] = {1};
    int a2[] = {1, 2, 3};

    // 先定义再赋值
    // 赋值只能索引赋值,不能直接输入元组
    int a3[3];
    a3[0] = 1;
    a3[1] = 2;
    a3[2] = 3;

    printf("%d", a1[0]);
}

int main() {
    array();
}